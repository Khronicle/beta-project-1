export interface Coordinates {
  latitude: number;
  longitude: number;
}

export interface Location {
  name: string;
  country: string;
  admin1?: string;
  latitude: number;
  longitude: number;
}

export interface CurrentWeather {
  temperature: number;
  apparentTemperature: number;
  humidity: number;
  windSpeed: number;
  weatherCode: number;
  isDay: boolean;
  time: string;
}

export interface DailyForecast {
  date: string;
  code: number;
  temperatureMax: number;
  temperatureMin: number;
}

export interface WeatherData {
  location: Location;
  current: CurrentWeather;
  daily: DailyForecast[];
}

